public class Calculator {
    
    /** Adds the 2 integer parameters and returns the 
     *  resulting sum.
     *@param x - value 1.
     *@param y - value 2.
     *@return - the sum of the input arguments.
     */
    public Integer add(Integer x, Integer y){
       return x + y;
    }
    
    /** Multiplies the integer parameters and returns the
     *  result.
     *@param x - value 1.
     *@param y - value 2.
     *@return - the product of x and y.
     */
    public Integer multiply(Integer x, Integer y){
        return x * y;
    }
}
